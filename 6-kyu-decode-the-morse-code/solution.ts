const decodeLetter = (encodedLetter: string) => MORSE_CODE[encodedLetter];

const decodeWord = (encodedWord: string) => encodedWord.split(' ').map(decodeLetter).join('');

export function decodeMorse(morseCode: string): string {
  return morseCode.trim().split('  ').map(decodeWord).join(' ');
}
